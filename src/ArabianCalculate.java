class ArabianCalculate {
    public static int plusArabian(String num1, String num2) {
        return Integer.parseInt(num1) + Integer.parseInt(num2);
    }

    public static int minusArabian(String num1, String num2) {
        return Integer.parseInt(num1) - Integer.parseInt(num2);
    }

    public static int multiplyArabian(String num1, String num2) {
        return Integer.parseInt(num1) * Integer.parseInt(num2);
    }

    public static int divideArabian(String num1, String num2) {
        return Integer.parseInt(num1) / Integer.parseInt(num2);
    }
}
